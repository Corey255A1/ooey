const uint8_t* BitmapFont::get_glyph_for_char(char c) {
    if (c == ' ') {
        return kAsciiGlyphs[0];
    }
    if (c >= 'a' && c <= 'z') {
        c = static_cast<char>(c - 'a' + 'A');
    }
    if (c >= '0' && c <= '9') {
        return kAsciiGlyphs[1 + (c - '0')];
    }
    if (c >= 'A' && c <= 'Z') {
        return kAsciiGlyphs[11 + (c - 'A')];
    }
    switch (c) {
        case ':': return kAsciiGlyphs[37];
        case ',': return kAsciiGlyphs[38];
        case '-': return kAsciiGlyphs[39];
        case '.': return kAsciiGlyphs[40];
        case '?': return kAsciiGlyphs[41];
        default: return kAsciiGlyphs[0];
    }
}

int BitmapFont::get_font_scale(int font_size) {
    return std::max(1, font_size / 8);
}

int BitmapFont::get_glyph_width(int font_size) {
    return 6 * get_font_scale(font_size);
}

int BitmapFont::get_glyph_height(int font_size) {
    return 8 * get_font_scale(font_size);
}

Size BitmapFont::measure_text(const std::string& text, int font_size) {
    int width = static_cast<int>(text.length()) * get_glyph_width(font_size);
    int height = get_glyph_height(font_size);
    return Size{width, height};
}

